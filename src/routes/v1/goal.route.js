const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const goalValidation = require('../../validations/goal.validation');
const goalController = require('../../controllers/goal.controller');

const router = express.Router();

router
  .route('/')
  .get(auth(), validate(goalValidation.getGoals), goalController.getGoals)
  .post(auth(), validate(goalValidation.createGoal), goalController.createGoal);

router
  .route('/:goalId')
  .patch(auth(), validate(goalValidation.updateGoal), goalController.getGoal, goalController.updateGoal)
  .delete(auth(), validate(goalValidation.deleteGoal), goalController.getGoal, goalController.deleteGoal);

module.exports = router;

